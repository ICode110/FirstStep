        public static void FToC()
        {
            Console.WriteLine("请输入华氏温度(F)");
            string inputNumber = Console.ReadLine();
            if (inputNumber == null)
            {
                Console.WriteLine("输入为空，请重新输入");
            }
            else
            {
                double F = Convert.ToDouble(inputNumber);
                double C = (F - 32) * 5 / 9;
                //ToString方法可以格式化保留两位小数
                Console.WriteLine("您输入的温度(F)是：" + F + ",转换后的温度(C)是：" + C.ToString("0.00"));
